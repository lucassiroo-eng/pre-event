// Minimal in-house i18n. The locale is derived from the chosen country
// (read from localStorage under "pre-event-country") and rerenders on change
// thanks to useSyncExternalStore over a tiny external store.

import { useSyncExternalStore } from "react";

export type Locale = "en" | "fr" | "es" | "it" | "de" | "pt";

const COUNTRY_TO_LOCALE: Record<string, Locale> = {
  fr: "fr",
  es: "es",
  it: "it",
  de: "de",
  pt: "pt",
  br: "pt",
  mx: "es",
  ar: "es",
  ch: "fr",
  gb: "en",
  us: "en",
  other: "en",
};

export function localeForCountry(code: string | null | undefined): Locale {
  if (!code) return "en";
  return COUNTRY_TO_LOCALE[code.toLowerCase()] ?? "en";
}

// ── external store so React rerenders when the country (locale) changes ──
const listeners = new Set<() => void>();
let currentCountry: string =
  typeof window !== "undefined"
    ? window.localStorage.getItem("pre-event-country") ?? ""
    : "";

function emit() {
  for (const l of listeners) l();
}

export function setLocaleCountry(code: string) {
  currentCountry = code;
  try { window.localStorage.setItem("pre-event-country", code); } catch {}
  emit();
}

if (typeof window !== "undefined") {
  window.addEventListener("storage", (e) => {
    if (e.key === "pre-event-country") {
      currentCountry = e.newValue ?? "";
      emit();
    }
  });
}

function subscribe(cb: () => void) {
  listeners.add(cb);
  return () => listeners.delete(cb);
}
function getSnapshot() { return currentCountry; }

export function useLocale(): Locale {
  const country = useSyncExternalStore(subscribe, getSnapshot, getSnapshot);
  return localeForCountry(country);
}

