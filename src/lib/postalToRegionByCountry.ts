export function postalToRegion(country: string, postal: string | null | undefined): string {
  if (!postal) return "unknown";
  const map = maps[country.toLowerCase()];
  if (!map) return "unknown";
  const clean = String(postal).trim().replace(/\s+/g, "").toUpperCase();
  if (clean.length < 2) return "unknown";
  // Try 2-char prefix
  const prefix2 = clean.slice(0, 2);
  if (map[prefix2]) return map[prefix2];
  // Try 4-char prefix (PT)
  const prefix4 = clean.slice(0, 4);
  if (map[prefix4]) return map[prefix4];
  return "unknown";
}
