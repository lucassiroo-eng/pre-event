// Postal code → region code mapping for all supported countries.
// Deterministic — first N digits of postal code map to a region.

type PostalMap = Record<string, string>;
const maps: Record<string, PostalMap> = {};

function add(country: string, code: string, prefixes: string[]) {
  if (!maps[country]) maps[country] = {};
  for (const p of prefixes) maps[country][p] = code;
}

